def pickingNumbers(a):
    # Write your code here
    a.sort()
    out=1
    for i in range(len(a)):
        for j in range(i,len(a)):
            if abs(a[j]-a[i])<=1:
                out = max(out,j-i+1)
            else:
                break
    return out
                
            

